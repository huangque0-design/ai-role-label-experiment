export function getAdviceAnalysis(question, recommendCorrect) {
  if (!question?.analysis) return null;

  const adviceAnswer = recommendCorrect ? question.correctAnswer : question.wrongAnswer;

  return {
    answer: adviceAnswer,
    text: recommendCorrect ? question.analysis.correct : question.analysis.incorrect,
    optionNotes: recommendCorrect
      ? question.analysis.correctOptionNotes
      : question.analysis.incorrectOptionNotes
  };
}
